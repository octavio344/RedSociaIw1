    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
       Usuario u=new Usuario();
       Usuario us=new Usuario();
       try{
        String pass=new String(jPassword.getPassword());
        String user=new String(jTextField1.getText());
        
        
        u.setPassword(pass);
        u.setUsername(user);
        u.setPusuaruio(null);
        
        us=lg.iniciarSecion(u);}
        catch(InvalidUserException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        catch(NullPointerException ex){
                        JOptionPane.showMessageDialog(this,"No completo el campo!");
        }
        if(us.getUsername()!=null&&us.getPassword()!=null){
            JOptionPane.showMessageDialog(this,"Bienvenido "+ u.getUsername()+" a OctiBook");
        }
       
    }//GEN-LAST:event_jButton1MouseClicked

